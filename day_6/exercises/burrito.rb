class Burrito
  attr_reader :protein, :base, :toppings
  def initialize(protein, base, toppings)
    @protein  = protein
    @base     = base
    @toppings = toppings
  end

  def add_topp
    toppings.insert(3, "cilantro")
  end

  def del_topp
    toppings.drop(2)
  end

  def change_protein
    protein.replace("Steak")
  end
end

